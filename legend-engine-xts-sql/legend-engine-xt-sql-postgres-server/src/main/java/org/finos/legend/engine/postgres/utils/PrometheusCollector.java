package org.finos.legend.engine.postgres.utils;

import io.prometheus.metrics.core.metrics.Counter;
import io.prometheus.metrics.core.metrics.Gauge;
import io.prometheus.metrics.core.metrics.Histogram;
import io.prometheus.metrics.model.snapshots.Unit;

public class PrometheusCollector
{

    public static final Gauge ACTIVE_SESSIONS = Gauge.builder()
            .name("current_active_sessions")
            .help("Number of active sessions")
            .register();
    public static final Counter TOTAL_SESSIONS = Counter.builder()
            .name("total_active_sessions")
            .help("Total of active sessions")
            .register();
    public static final Gauge ACTIVE_EXECUTE = Gauge.builder()
            .name("active_sessions")
            .help("Number of active execute requests")
            .register();

    public static final Counter TOTAL_EXECUTE = Counter.builder()
            .name("total_execute_requests")
            .help("Total of execute requests")
            .register();
    public static final Counter TOTAL_SUCCESS_EXECUTE = Counter.builder()
            .name("total_failure_execute_requests")
            .help("Total of failure execute requests")
            .register();
    public static final Counter TOTAL_FAILURE_EXECUTE = Counter.builder()
            .name("total_success_execute_requests")
            .help("Total of success execute requests")
            .register();

    public static final Histogram EXECUTE_DURATION = Histogram.builder()
            .name("execute_requests_duration")
            .help("Execute duration")
            .unit(Unit.SECONDS)
            .register();

    public static final Gauge ACTIVE_METADATA = Gauge.builder()
            .name("active_metadata_requests")
            .help("Number of active metadata requests")
            .register();
    public static final Counter TOTAL_METADATA = Counter.builder()
            .name("total_metadata_requests")
            .help("Total of metadata requests")
            .register();
    public static final Counter TOTAL_SUCCESS_METADATA = Counter.builder()
            .name("total_success_metadata_requests")
            .help("Total of success metadata requests")
            .register();
    public static final Counter TOTAL_FAILURE_METADATA = Counter.builder()
            .name("total_failure_metadata_requests")
            .help("Total of failure metadata requests")
            .register();

    public static final Histogram METADATA_DURATION = Histogram.builder()
            .name("metadata_requests_duration")
            .help("Execute duration")
            .unit(Unit.SECONDS)
            .register();


}
